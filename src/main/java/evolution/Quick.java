package main.java.evolution;

public class Quick extends Evolution {
    
    /**
     * the constructor for the quick evolution.
     */
    public Quick() {
        this.name = "Quick";
        this.healthModifier = 0;
        this.attackModifier = 1.0;
        this.defenseModifier = 0;
        this.speedModifier = 2;
        this.accuracyModifier = 1.0;
        this.experienceRateModifier = 1.0;
    }

}
