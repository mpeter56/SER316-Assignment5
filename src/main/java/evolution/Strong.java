package main.java.evolution;

public class Strong extends Evolution {
    
    /**
     * the constructor for the strong evolution.
     */
    public Strong() {
        this.name = "Strong";
        this.healthModifier = 0;
        this.attackModifier = 1.2;
        this.defenseModifier = 0;
        this.speedModifier = 0;
        this.accuracyModifier = 1.0;
        this.experienceRateModifier = 1.0;
    }

}
