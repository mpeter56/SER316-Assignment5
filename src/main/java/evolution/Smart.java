package main.java.evolution;

public class Smart extends Evolution {
    
    /**
     * the constructor for the smart evolution.
     */
    public Smart() {
        this.name = "Smart";
        this.healthModifier = 0;
        this.attackModifier = 1.0;
        this.defenseModifier = 0;
        this.speedModifier = 0;
        this.accuracyModifier = 1.0;
        this.experienceRateModifier = 0.8;
    }

}
