package main.java.evolution;

public class Tough extends Evolution {
    
    /**
     * the constructor for the tough evolution.
     */
    public Tough() {
        this.name = "Tough";
        this.healthModifier = 0;
        this.attackModifier = 1.0;
        this.defenseModifier = 2;
        this.speedModifier = 0;
        this.accuracyModifier = 1.0;
        this.experienceRateModifier = 1.0;
    }

}
