package main.java.evolution;

public class Hearty extends Evolution {
    /**
     * the constructor for the hearty evolution.
     */
    public Hearty() {
        this.name = "Hearty";
        this.healthModifier = 20;
        this.attackModifier = 1.0;
        this.defenseModifier = 0;
        this.speedModifier = 0;
        this.accuracyModifier = 1.0;
        this.experienceRateModifier = 1.0;
    }

}
