package main.java.evolution;

public class Careful extends Evolution {
    
    /**
     * the constructor for the careful evolution.
     */
    public Careful() {
        this.name = "Careful";
        this.healthModifier = 0;
        this.attackModifier = 1.0;
        this.defenseModifier = 0;
        this.speedModifier = 0;
        this.accuracyModifier = 1.2;
        this.experienceRateModifier = 1.0;
    }

}
